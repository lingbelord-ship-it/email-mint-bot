-- Create a table to store failed email attempts
CREATE TABLE IF NOT EXISTS public.failed_emails (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  email TEXT NOT NULL UNIQUE,
  reason TEXT,
  failed_at TIMESTAMP WITH TIME ZONE DEFAULT now(),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.failed_emails ENABLE ROW LEVEL SECURITY;

-- Create policies for failed_emails
CREATE POLICY "Allow public read access to failed_emails" 
ON public.failed_emails 
FOR SELECT 
USING (true);

CREATE POLICY "Allow public insert to failed_emails" 
ON public.failed_emails 
FOR INSERT 
WITH CHECK (true);

-- Create index for faster lookups
CREATE INDEX IF NOT EXISTS idx_failed_emails_email ON public.failed_emails(email);